//-----------------------------------------------------------------------------
// The function of 60 port
//  Note :
//  KBHIStep != 0x00 is necessary.
//-----------------------------------------------------------------------------
void Hook_60Port(BYTE KBHICMD)
{
    KBC6064_RamDebug(0x60);
    KBC6064_RamDebug(KBHIData);

    switch (KBHICMD)
    {
    case 0x80:
        OEM_Cmd_80Data();
        break;

    case 0x81:
        OEM_Cmd_81Data();
        break;

    case 0xD9:
        OEM_Cmd_D9Data();
        break;


    default:
        break;
    }
}

//-----------------------------------------------------------------------------
// The function of 64 port
//-----------------------------------------------------------------------------
void Hook_64Port(BYTE KBHICmd)
{
    KBC6064_RamDebug(0x64);
    KBC6064_RamDebug(KBHICmd);

    switch (KBHICmd)
    {
    case 0x80:
        OEM_Cmd_80();
        break;

    case 0x81:
        OEM_Cmd_81();
        break;

    case 0x94:
        OEM_Cmd_94();
        break;

    case 0xA2:
        OEM_Cmd_A2();
        break;

    case 0xB4:
        OEM_Cmd_B4();
        break;

    case 0xC5:
        OEM_Cmd_C5();
        break;

    case 0xC9:
        OEM_Cmd_C9();
        break;

    case 0xD8:
        OEM_Cmd_D8();
        break;

    case 0xD9:
        OEM_Cmd_D9();
        break;

    case 0xDD:
        OEM_Cmd_DD();
        break;

    default:
        break;
    }
}

